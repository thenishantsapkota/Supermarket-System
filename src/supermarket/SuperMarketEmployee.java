    private void handleEmployees() {
        try {
            String sql = "select * from employee_tbl";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                String id = String.valueOf(rs.getInt("id"));
                String name = rs.getString("name");
                String position = rs.getString("position");

                String tableData[] = { id, name, position };
                DefaultTableModel tblModel = (DefaultTableModel) myTable.getModel();
                tblModel.addRow(tableData);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void mouseEvent() {
        myTable.addMouseListener(new MouseListener() {
            @Override
            public void mouseClicked(MouseEvent e) {
            }

            @Override
            public void mousePressed(MouseEvent e) {
                try {
                    String id = (String) myTable.getValueAt(myTable.getSelectedRow(), 0);
                    myId = id;
                    String getSql = "select * from employee_tbl where id=?";
                    ps = con.prepareStatement(getSql);
                    ps.setString(1, myId);
                    rs = ps.executeQuery();
                    if (rs.next()) {
                        employeeName.setText(rs.getString("name"));
                        employeePosition.setText(rs.getString("position"));
                        ps.close();
                        rs.close();
                    }

                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }
            }

            @Override
            public void mouseReleased(MouseEvent e) {
            }

            @Override
            public void mouseEntered(MouseEvent e) {
            }

            @Override
            public void mouseExited(MouseEvent e) {
            }
        });

    }

